# Lists can be combined using the addition operator (+):
num_list = [1, 2, 3]
char_list = ['a', 'b', 'c']
print(num_list + char_list)
#=================================
print('\n')
# multiplied using the multiplication operator (*):
list_a = ['a', 'b', 'c']
print(list_a * 2)
#=================================
print('\n')
# using the in operator:
num_list = [2, 4, 6]
print(5 in num_list)
print(5 not in num_list)